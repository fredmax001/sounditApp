def add_import(content, after):
    return content.replace(after, after + "\nimport { useTranslation } from 'react-i18next';")

def add_hook(content, after):
    return content.replace(after, after + "\n  const { t } = useTranslation();")

def process(filename, page_key, import_after, hook_after, extra_replacements, keys):
    path = os.path.join(BASE, filename)
    with open(path, "r") as f:
        content = f.read()
    content = add_import(content, import_after)
    content = add_hook(content, hook_after)
    for old, new in extra_replacements:
        content = content.replace(old, new)
    with open(path, "w") as f:
        f.write(content)
    all_keys["admin"][page_key] = keys

